-- =============================================================================
-- TABLE: jackpots
-- Description: Stores jackpot pools and strategy configurations (Fixed/Variable)
-- =============================================================================

CREATE TABLE jackpots (
    id VARCHAR(64) PRIMARY KEY,
    name VARCHAR(120) NOT NULL,
    current_pool DECIMAL(19, 2) NOT NULL,
    initial_pool DECIMAL(19, 2) NOT NULL,
    contribution_type VARCHAR(16) NOT NULL,
    fixed_contribution_rate DECIMAL(19, 10),
    variable_initial_rate DECIMAL(19, 10),
    variable_min_rate DECIMAL(19, 10),
    variable_decrement_rate DECIMAL(19, 10),
    reward_type VARCHAR(16) NOT NULL,
    fixed_reward_chance DECIMAL(19, 10),
    variable_initial_chance DECIMAL(19, 10),
    variable_coefficient DECIMAL(19, 10),
    variable_limit_pool DECIMAL(19, 2),
    version BIGINT NOT NULL DEFAULT 0
);

COMMENT ON TABLE jackpots IS 'Stores jackpot pools and strategy configurations (Fixed/Variable)';

COMMENT ON COLUMN jackpots.id IS 'Unique identifier of the jackpot pool';
COMMENT ON COLUMN jackpots.name IS 'Human-readable name of the jackpot';
COMMENT ON COLUMN jackpots.current_pool IS 'Current balance of the jackpot pool';
COMMENT ON COLUMN jackpots.initial_pool IS 'Initial balance of the jackpot pool (reset value after a win)';
COMMENT ON COLUMN jackpots.contribution_type IS 'Type of contribution strategy (e.g., FIXED, VARIABLE)';
COMMENT ON COLUMN jackpots.fixed_contribution_rate IS 'Fixed contribution rate as a fraction of the bet stake';
COMMENT ON COLUMN jackpots.variable_initial_rate IS 'Initial contribution rate for variable strategy';
COMMENT ON COLUMN jackpots.variable_min_rate IS 'Minimum contribution rate for variable strategy';
COMMENT ON COLUMN jackpots.variable_decrement_rate IS 'Rate reduction per one monetary unit of pool growth';
COMMENT ON COLUMN jackpots.reward_type IS 'Type of reward strategy (e.g., FIXED, VARIABLE)';
COMMENT ON COLUMN jackpots.fixed_reward_chance IS 'Fixed winning probability as a fraction of 1.0';
COMMENT ON COLUMN jackpots.variable_initial_chance IS 'Initial winning probability for variable strategy';
COMMENT ON COLUMN jackpots.variable_coefficient IS 'Probability increase per one monetary unit of pool growth';
COMMENT ON COLUMN jackpots.variable_limit_pool IS 'Pool amount limit at which winning probability becomes 1.0';
COMMENT ON COLUMN jackpots.version IS 'Optimistic locking version for concurrent access control';

--rollback DROP TABLE jackpots;


