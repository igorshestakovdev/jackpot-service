-- =============================================================================
-- TABLE: jackpot_contributions
-- Description: Tracks contributions to jackpots made by individual bets
-- =============================================================================

CREATE TABLE jackpot_contributions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    bet_id VARCHAR(64) NOT NULL UNIQUE,
    user_id VARCHAR(64) NOT NULL,
    jackpot_id VARCHAR(64) NOT NULL,
    stake_amount DECIMAL(19, 2) NOT NULL,
    contribution_amount DECIMAL(19, 2) NOT NULL,
    current_jackpot_amount DECIMAL(19, 2) NOT NULL,
    created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_contribution_jackpot FOREIGN KEY (jackpot_id) REFERENCES jackpots(id)
);

COMMENT ON TABLE jackpot_contributions IS 'Tracks historical bet contributions to jackpot pools';

COMMENT ON COLUMN jackpot_contributions.id IS 'Unique identifier of the contribution record';
COMMENT ON COLUMN jackpot_contributions.bet_id IS 'Unique identifier of the bet that triggered this contribution';
COMMENT ON COLUMN jackpot_contributions.user_id IS 'Unique identifier of the user who placed the bet';
COMMENT ON COLUMN jackpot_contributions.jackpot_id IS 'Identifier of the jackpot pool to which the contribution was made';
COMMENT ON COLUMN jackpot_contributions.stake_amount IS 'Stake amount of the bet';
COMMENT ON COLUMN jackpot_contributions.contribution_amount IS 'Amount contributed to the jackpot pool';
COMMENT ON COLUMN jackpot_contributions.current_jackpot_amount IS 'Jackpot pool amount immediately after this contribution';
COMMENT ON COLUMN jackpot_contributions.created_at IS 'Timestamp when the contribution was recorded';

CREATE INDEX idx_contributions_jackpot_id ON jackpot_contributions(jackpot_id);

--rollback DROP TABLE jackpot_contributions;


