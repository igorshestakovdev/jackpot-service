-- =============================================================================
-- TABLE: jackpot_reward_evaluations
-- Description: Records the lottery evaluation results (won/lost) for each bet
-- =============================================================================

CREATE TABLE jackpot_reward_evaluations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    bet_id VARCHAR(64) NOT NULL UNIQUE,
    user_id VARCHAR(64) NOT NULL,
    jackpot_id VARCHAR(64) NOT NULL,
    won BOOLEAN NOT NULL,
    evaluated_chance DECIMAL(19, 10) NOT NULL,
    random_value DECIMAL(19, 10) NOT NULL,
    reward_amount DECIMAL(19, 2),
    created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_evaluation_jackpot FOREIGN KEY (jackpot_id) REFERENCES jackpots(id),
    CONSTRAINT fk_evaluation_contribution FOREIGN KEY (bet_id) REFERENCES jackpot_contributions(bet_id)
);

COMMENT ON TABLE jackpot_reward_evaluations IS 'Records lottery evaluation results (won/lost) for each bet';

COMMENT ON COLUMN jackpot_reward_evaluations.id IS 'Unique identifier of the evaluation record';
COMMENT ON COLUMN jackpot_reward_evaluations.bet_id IS 'Unique identifier of the evaluated bet';
COMMENT ON COLUMN jackpot_reward_evaluations.user_id IS 'Unique identifier of the user who placed the bet';
COMMENT ON COLUMN jackpot_reward_evaluations.jackpot_id IS 'Identifier of the evaluated jackpot pool';
COMMENT ON COLUMN jackpot_reward_evaluations.won IS 'Boolean flag indicating whether the bet won the jackpot';
COMMENT ON COLUMN jackpot_reward_evaluations.evaluated_chance IS 'Calculated probability of winning at the moment of evaluation';
COMMENT ON COLUMN jackpot_reward_evaluations.random_value IS 'Random value generated for evaluation (win if random_value < evaluated_chance)';
COMMENT ON COLUMN jackpot_reward_evaluations.reward_amount IS 'Reward amount paid out (if won)';
COMMENT ON COLUMN jackpot_reward_evaluations.created_at IS 'Timestamp when the evaluation was performed';

CREATE INDEX idx_evaluations_jackpot_id ON jackpot_reward_evaluations(jackpot_id);
CREATE INDEX idx_evaluations_bet_id ON jackpot_reward_evaluations(bet_id);

--rollback DROP TABLE jackpot_reward_evaluations;
